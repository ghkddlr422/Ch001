package Ch001;

public class PyramidTest2 {

	public static void main(String[] args) {
		for(int i =1; i <=5; i++) {
			printStar(i);
			System.out.println();
		}
	}
	
	public static void printStar(int star) {
		for(int a = 0; a<star; a++)
		{
			System.out.print("*");
			
	}
	

	}
}
